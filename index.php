                                    <li><strong>
                                            <font>
                                                <font><span class="blue">Nigeria</span><span class="blue">:</span></font>
                                            </font>
                                        </strong>
                                        <font>
                                            <font>&nbsp;The terror unleashed by Boko Haram militants triggered a mass exodus in northeastern Nigeria. When the Nigerian army recaptured the area in 2016, the scale of the refugee and hunger crisis became apparent.</font>
                                        </font>
                                    </li>
                                    <li><span class="blue"><strong>
                                                <font>
                                                    <font>Somalia:</font>
                                                </font>
                                            </strong></span>
                                        <font>
                                            <font>&nbsp;Located in the Horn of Africa, Somalia is marked by decades of civil war and anarchy. Now Somalia has been hit by a devastating drought and related famine. This is even more drastic than the 2011-2012 famine</font>
                                        </font>
                                    </li>
                                    <li><span class="blue"><strong>
                                                <font>
                                                    <font>South Sudan:</font>
                                                </font>
                                            </strong></span>
                                        <font>
                                            <font>&nbsp;In the north of South Sudan, famine prevails: on February 2, 2017, the United Nations officially proclaimed a hunger emergency. The country’s civil war, which has been raging for years, leaves fields fallow and blocks aid deliveries.</font>
                                        </font>
                                    </li>
                                    <li><span class="blue"><strong>
                                                <font>
                                                    <font>Yemen:</font>
                                                </font>
                                            </strong></span>
                                        <font>
                                            <font>&nbsp;The "poor house" of the Arab world is the only non-African country that is currently threatened by a famine.&nbsp;</font>
                                            <font>Since 2015, Yemen has been shaken by a civil war.&nbsp;Hunger is used as a weapon against the civilian population.</font>
                                        </font>
                                    </li>
                                </ul>
                                <p>
                                    <font>
                                        <font>​</font>
                                    </font>
                                </p>
