                                        <font>In addition, people in many African countries have recently been suffering from the effects of the weather phenomenon El Niño: droughts or torrential rains destroy crops, kill cattle and lead to starvation.</font>
                                    </font><br>
                                    <br>
                                    <font>
                                        <font>In 2017, <span class="blue">37 countries, including 28 in Africa, depended on food aid, according to the Food and Agriculture Organization of the United Nations (FAO)</span>.&nbsp;</font><br>
                                        <br>
                                        <font>These countries are:&nbsp;</font>
                                    </font><span class="blue">
                                        <font>
                                            <font>Afghanistan, Burkina Faso, Burundi, Cameroon, Central African Republic, Chad, Congo, Djibouti, DR Congo [or Democratic Republic of the Congo], Eritrea, Ethiopia, Guinea, Haiti, Iraq, Kenya, Lesotho, Liberia, Libya, Madagascar, Malawi, Mali, Mauritania, Mozambique, Myanmar, Niger, Nigeria, North Korea, Pakistan, Sierra Leone, Somalia, South Sudan, Sudan, Swaziland, Syria, Uganda, Yemen and Zimbabwe.</font>
                                        </font>
                                    </span>
                                </p>
