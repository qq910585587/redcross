                                    <font>
                                        <font>Hunger and famine crises are escalating at several hot spots: in four countries – three in Africa and one in the Middle East – a total of&nbsp;20 million people,&nbsp;including countless children, are at risk of starvation. These four crisis areas, all suffering from the consequences of armed conflict, are:</font>
                                    </font>
                                </p>
