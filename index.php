                                    <font>
                                        <font>But the United Nations lacks the money to provide much-needed aid to millions of hungry people: donations promised by the international community have not yet been made.</font>
                                    </font>
                                </p>
                                <p>&nbsp;</p>
                                <h2 style="text-align: center;"><strong><span class="blue">FAMINE AND CHRONIC HUNGER</span></strong></h2>
                                <p style="text-align: center;">&nbsp;</p>
                                <p>
                                    <font>
                                        <font>Famines are acute food crises, usually after drought or due to armed conflict. Famine is the worst form of food shortage. In addition to old people, babies and small children are especially threatened by starvation. According to the United Nations definition (Integrated Food Security Phase Classification), there is a famine if at least:</font>
                                    </font>
                                </p>
                                <p>&nbsp;</p>
                                <ul class="square-bullet-list">
                                    <li>
                                        <font>
                                            <font><span class="blue">20%</span>&nbsp;of households suffer from extreme food shortages,</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">30%</span>&nbsp;of the population is&nbsp;acutely malnourished; and</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">Two out of&nbsp;every 10,000 people</span>, or four children, die daily from food shortages.</font>
                                        </font>
                                    </li>
                                </ul>
                                <p>&nbsp;</p>
                                <p>
                                    <font>
                                        <font>One of the worst hunger crises&nbsp;of the past 25 years was the famine in East Africa in 2011/12.&nbsp;</font>
                                        <font>In war-torn&nbsp;Somalia, <span class="blue">260,000 people starved to death</span>, including <span class="blue">133,000 children under the age of five</span>.</font>
                                    </font>
                                </p>
                                <p>
                                    <font>
                                        <font>Sub-Saharan Africa is also a hotbed of chronic hunger due to extreme poverty.&nbsp;According to the FAO definition, people suffer from chronic hunger if their daily energy intake for an extended period of time is below what they would need for a healthy and active life. The lower limit is an average of 1,800 calories per day.&nbsp;</font>
                                    </font><br>
                                    <br>
                                    <font>
                                        <font>According to this measure, <span class="blue">226.7 million people are starving in Africa</span>. The countries most affected by extreme poverty and hunger in Africa are mainly those located south of the Sahara. One in four people suffers from hunger there – which means that the share of the world's hungry is highest in sub-Saharan Africa.</font>
                                    </font>
                                </p>
                                <p>
                                    <font>
                                        <font>In the sub-Saharan&nbsp;region, <span class="blue">40% to 50% of people live below the poverty line, meaning they have a daily income that is on average below $1.25</span>.&nbsp;</font>
                                        <font>This means that sub-Saharan Africa, along with southern Asia, is one of the poorest regions in the world.</font>
                                    </font>
                                </p>
                                <p style="text-align: center;">&nbsp;</p>
                                <p style="text-align: center;">
                                    </p>
                                <p></p>
                                <p style="text-align: center;">&nbsp;</p>
                                <h2 style="text-align: center;"><strong><span class="blue">MALNUTRITION AND HIGH INFANT MORTALITY</span></strong></h2>
                                <p>&nbsp;</p>
                                <p>
                                    <font>
                                        <font>Children are particularly affected by the hunger crisis&nbsp;in Africa. There are far too many starving kids in Africa, every single affected kid&nbsp;is one too much.&nbsp;Malnutrition leads to physical and mental development delays and&nbsp;disorders and is a major cause of high infant mortality rates in sub-Saharan Africa.</font>
                                    </font>
                                </p>
                                <p>&nbsp;</p>
                                <ul class="square-bullet-list">
                                    <li>
                                        <font>
                                            <font>According to UN data, <span class="blue">165 million children</span> worldwide are too small&nbsp;for their age, or stunted, due to chronic malnutrition.&nbsp;</font>
                                            <font>Three quarters of these children live in sub-Saharan Africa and South Asia.&nbsp;</font><span class="blue">
                                                <font>In sub-Saharan Africa, 40%&nbsp;of children are affected;&nbsp;in South Asia, 39%.</font>
                                            </span>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">3.2 million children under the age of 5&nbsp;die each year</span> in sub-Saharan Africa - that's about half of the world's deaths in this age group.</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font>Worldwide, nearly every second death in children under the age of five is due to malnutrition.&nbsp;</font>
                                            <font>As this weakens the immune system, diseases such as pneumonia, malaria or diarrhea often lead to death.</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font>The sub-Saharan child mortality rate is one of the highest in the world, with one in nine children dying before the age of 5. In Sierra Leone, one in every six children dies before the age of 5. Especially young children die in Nigeria and the Democratic Republic of the Congo.</font>
                                        </font>
                                    </li>
                                </ul>
                                <p style="text-align: center;">&nbsp;</p>
                                <h2 style="text-align: center;"><strong><span class="blue">HUNGER AND FOOD SCARCENESS IN AFRICA HAS MANY CAUSES</span></strong></h2>
                                <p>&nbsp;</p>
                                <p>
                                    <font>
                                        <font>The reasons for the widespread hunger and food scarceness in Africa are complex and are not, as often assumed, a lack of agricultural productivity or difficult climatic conditions. Sub-Saharan Africa has millions of hectares of fertile soil. The African continent could feed itself. However, several factors prevent self-sufficiency and a victory in the fight against hunger in Africa:</font>
                                    </font>
                                </p>
                                <p>&nbsp;</p>
                                <ul class="square-bullet-list">
                                    <li>
                                        <font>
                                            <font><span class="blue">Population growth:</span> In sub-Saharan Africa, the number of people is growing rapidly, but food production is not keeping up.</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">Unfair trading structures:</span> EU and US subsidize domestic agriculture, African farmers are not competitive with cheap food imports.</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">Debt trap and mismanagement: </span>The high level of indebtedness of many African countries as well as poor governance and corruption is&nbsp;blocking economic development.&nbsp;</font>
                                            <font>Mass poverty and hunger are the consequences.</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">Diseases:</span> The AIDS epidemic, as well as dieseases like malaria, inhibits agricultural production in Africa and takes&nbsp;breadwinners from their families.</font>
                                        </font>
                                    </li>
                                    <li>
                                        <font>
                                            <font><span class="blue">Armed conflicts:</span> Africa has more than its share</font>
                                        </font>
                                        <font>
                                            <font>&nbsp;of trouble spots.&nbsp;</font>
                                            <font>Most wars in the world&nbsp;rage south of the Sahara.&nbsp;</font>
                                            <font>Refugee misery and hunger are the companions.</font>
                                        </font>
                                    </li>
                                </ul>
                                <p>
                                    <font>
                                        <font>​</font>
                                    </font>
                                </p>
        
                                <p style="text-align: center;">&nbsp;</p>
                                <h2 style="text-align: center;"><strong><span class="blue">FIGHT AGAINST HUNGER AND FOOD SCARCENESS IN AFRICA - UN MILLENNIUM GOAL NARROWLY MISSED</span></strong></h2>
                                <p>&nbsp;</p>
                                <p>
                                    <font>
                                        <font>In 2000, the 55th UN General Assembly, the Millennium Summit, took place in New York.&nbsp;</font>
                                        <font>On September 9, 2000, the <span class="blue">189 UN member states</span> adopted a declaration with the&nbsp;UN Millennium Development Goals that&nbsp;the proportion of malnourished people should be halved by 2015. T</font>
                                        <font>his goal was narrowly missed.&nbsp;</font>
                                        <font>One in eight people in developing regions is starving today (<span class="blue">12.9%&nbsp;in 2014-2016</span>).&nbsp;</font>
                                        <font>This is due in particular to the fact that the population is growing strongly in sub-Saharan African countries.</font>
                                    </font>
                                </p>
                                <p>
                                    <font>
                                        <font>The fight against hunger and famine remains one of the biggest challenges facing the world community.&nbsp;By 2030, the United Nations wants to end hunger.&nbsp;This was enshrined in the&nbsp;Sustainable Development Goals (SDGs), which the UN adopted in 2015 as a follow-up to the Millennium Development Goals.</font>
                                    </font>
                                </p>
                                <p style="text-align: center;">&nbsp;</p>
                                <h2 style="text-align: center;"><span class="blue"><strong>SUCCESSES AND REGRESSION IN THE FIGHT AGAINST HUNGER AND FAMINE IN AFRICA</strong></span></h2>
                                <p style="text-align: center;">&nbsp;</p>
                                <p>
                                    <font>
                                        <font>Efforts to counteract hunger and food scarceness&nbsp;in Africa have been marked by significant successes, but also by devastating setbacks.&nbsp;</font>
                                    </font>
                                    <font>
                                        <font>The most important achievements include:</font>
                                    </font>
                                </p>
                                <p>&nbsp;</p>
                                <ul class="square-bullet-list">
                                    <li>Greater political stability in countries formerly experiencing civil war</li>
                                    <li>Economic growth of the sub-Saharan region</li>
                                    <li>Progress in the fight against HIV/AIDS and malaria</li>
                                    <li>Reduction in the infant mortality rate</li>
                                    <li>Decreases in the fight against hunger and famine in Africa include:</li>
                                    <li>Crop failure due to climate change and environmental degradation</li>
                                    <li>Rising food prices and food speculation worldwide</li>
                                    <li>Humanitarian crises due to armed conflicts in northern Mali, northern Nigeria and South Sudan</li>
                                </ul>
                                <p>&nbsp;</p>
                                <h2 style="text-align: center;"><span class="blue"><strong>SOS CHILDREN'S VILLAGES HELPS THE HUNGRY IN AFRICA</strong></span></h2>
                                <p style="text-align: center;">&nbsp;</p>
                                <p>
                                    <font>
                                        <font>SOS Children's Villages is&nbsp;active in 46 African countries. A total of</font>
                                        <font>&nbsp;147 SOS villages in Africa prived homes for&nbsp;orphaned and abandoned children who&nbsp;would otherwise be in acute danger of starvation and malnutrition.&nbsp;</font>
                                        <font>In the fight against hunger in Africa, SOS is also involved in long-term development projects and humanitarian aid:</font>
                                    </font><br>
                                    <br>
                                    <font>
                                        <font>Family strengthening programs: SOS Children’s Villages helps starving and food-insecure families by pairing short-term aid (food, access to medical care, school supplies for children) with long-term support and guidance on income generation, childrearing, and financial and household management.</font>
                                    </font><br>
                                    <br>
                                    <font>
                                        <font>Emergency aid SOS Children's Villages currently provides emergency relief for hunger crises and famine in Somalia, Nigeria, South Sudan, Ethiopia and Malawi. Other examples of recent SOS emergency actions are 2005 and 2010 in Niger, 2011-2012 in East Africa and 2012 in the Sahel region.</font>
                                    </font>
                                </p>
